public class ApplyOperator {

    public static void main(String[] args) {

        // declarations
        int x = 7, result;
        char c = 'B', incrementChar;

        // operation
        result = ++x * 8;
        incrementChar = ++c; // result will be c

        // display results
        System.out.println(result);
        System.out.println(incrementChar);
    }

}
